<h2 class="box-header">HOW IT WORKS – PSA</h2>
										
<p>
PSA oxygen generators utilize the well-known and mature Pressure Swing Adsorption technology.
 Atmospheric air comprises of 20-21% oxygen and the PSA process utilizes Zeolite molecular sieves
 to extract this Oxygen from air. Oxygen at 95% is delivered, while the nitrogen adsorbed by the molecular 
 sieves is vented back into air through the exhaust line.

PSA process consists of 2 vessels filled with Molecular Sieves and Activated Alumina. 
Clean compressed air at 30° C temperature is passed through one vessel and oxygen comes 
out as product gas. The exhaust gas (Nitrogen) is discharged back into atmosphere. Upon 
saturation of the molecular sieve bed, the process switches Oxygen generation to the other 
bed by automatic valves, while allowing the saturated bed to undergo regeneration by depressurization 
and purging to atmospheric pressure. Thus 2-vessels keep cycling alternately in
 Oxygen production and regeneration ensuring Oxygen gas is continuously available to your process.
</p>


<h2 class="box-header">HOW IT WORKS – VPSA</h2>
										
<p>
For higher flow requirements, we recommend considering VPSA oxygen generators
 utilize the well-known and mature Vacuum Pressure Swing Adsorption technology. 
 Process for VPSA is identical to PSA with the differences highlighted below:
<h4>0 VPSA systems use low pressure blowers for better energy efficiency for higher air flows</h4>

<h4>0 In the regeneration step of the cycle, a vacuum pump is utilized to assist with optimal regeneration of molecular sieves</h4>

<h4>0 Due to lower working pressure, VPSA systems deliver Oxygen at a pressure of up to 1.3 kg/cm2.</h4>

<h4>0 Customers requiring higher pressure will need to opt for a booster compressor downstream of the VPSA system.</h4>



</p>



